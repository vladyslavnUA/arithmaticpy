def add(arg1: float, arg2: float) -> float:
    """
    
    Simple function to add two numbers (float) together
    All arguments must be of integers or float.
    :param arg1: first float value
    :param arg2: second float value
    :return: sum of the two arguments

    """
    
    # get two numbers and add them
    total = arg1 + arg2
    return total
